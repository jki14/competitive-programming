int n, ans;
int g[N][N];

int check(const pnt_t &x, const pnt_t &y, const pnt_t &p){
    int sgn = fsign((y-x)^(p-x));
    return (sgn==0)?0:1;
}

class BearBall {
    public:
    int countThrows(vector<int> x, vector<int> y) {
        n=SIZE(x);
        pnt_t *a=(pnt_t*)malloc(sizeof(pnt_t)*n);
        for(int i=0; i<n; i++){
            a[i].index=i;
            a[i].x=x[i];
            a[i].y=y[i];
        }
        int flag=1;
        memset(g, 0x3f, sizeof(g));
        for(int i=0; i<n; i++){
            polar_angle_sort(a+i, n-i);
            g[a[i].index][a[i].index]=0;
            for(int j=i+1; j<n; j++){
                if(j==i+1){
                    g[a[i].index][a[j].index]=1;
                    g[a[j].index][a[i].index]=1;
                }else if(check(a[i], a[j], a[j-1])){
                    g[a[i].index][a[j].index]=1;
                    g[a[j].index][a[i].index]=1;
                    flag=0;
                }
            }
        }
        ans=0;
        //printf("flag=%d\n", flag);
        if(flag){
            for(int i=0; i<n; i++){
                ans+=(i+1)*i/2;
                ans+=(n-i)*(n-i-1)/2;
            }
        }else{
            for(int i=0; i<n; i++){
                for(int j=0; j<n; j++){
                    if(g[i][j]>1)g[i][j]=2;
                    ans+=g[i][j];
                }
            }
        }
        free(a);
        return ans;
    }
};

