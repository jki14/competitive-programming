#include "bits/stdc++.h"
using namespace std;

class SwappingNodes {
  public:
  vector<int> swapNodes(vector<int> leaves, int numberOfLeaves) {
    const int n = numberOfLeaves;
    for (int k = 1; k < n; k <<= 1) {
      for (int p = 0; p < n; p += (k << 1)) {
        bool flag = false;
        for (int i = 0; i < k; ++i) {
          if (leaves[p + i] == leaves[p + k + i]) continue;
          if (leaves[p + i] > leaves[p + k + i]) {
            flag = true;
          }
          break;
        }
        if (flag) {
          for (int i = 0; i < k; ++i) {
            swap(leaves[p + i], leaves[p + k + i]);
          }
        }
      }
    }
    return leaves;
  }
};

