class FindingFriend {
    public:
    int find(int m, vector<int> a, int p) {
        int n=SIZE(a), ans=0;
        int num=n*m;
        sort(a.begin(), a.end(), greater<int>());
        for(int i=0; i<n; i++){
            if(a[i]==p)return 1;
            if(a[i]<p)ans++;
            if(num-a[i]-i==(i+1)*(m-1)){
                if(a[i]<p){
                    return ans;
                }else ans=0;
            }
        }
        return ans;
    }
};

