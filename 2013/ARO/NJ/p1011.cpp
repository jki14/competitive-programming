queue<state> sque;

void init(char *key,const int &d){
	int p,k;
	for(p=0;*key;key++){
		k=((*key)=='R')?0:1;
		if(!route[p][k]){
			route[p][k]=++num;
			flag[num]=route[num][0]=route[num][1]=0;
		}
		p=route[p][k];
	}
	flag[p]|=d;
}

void build(){
	int k,p,q,l,r;l=r=0;que[r++]=0;
	for(int i=0;i<=num;i++)fail[i]=0;
	for(;l<r;l++){
		p=que[l];
		for(k=0;k<=K;k++){
			if(route[p][k]){
				que[r++]=route[p][k];
				if(p){
					for(q=fail[p];q && !route[q][k];)q=fail[q];
					fail[route[p][k]]=route[q][k];
					flag[route[p][k]]|=flag[route[q][k]];
				}
			}
		}
	}
}

const int mod=1000000007;
const lld modL=1000000007LL;

void amod(int &x,const int &y){ x=(x+y)%1000000007; }

void init(){
	cc[0][0]=1;cc[0][1]=0;
	for(int i=1;i<maxnum;i++){cc[i][0]=cc[i][i]=1;
		for(int j=1;j<i;j++){
			cc[i][j]=(cc[i-1][j-1]+cc[i-1][j])%mod;
			//printf("cc[%d][%d]=%d\n",i,j,cc[i][j]);
		}
	}
}

