class LittleElephantAndRGB {
	public:
	long long getNumber(vector <string> list, int minGreen) {
		n=0;m=minGreen;ans=0LL;
		
		for(int i=0,x=_size(list);i<x;i++)
			for(int j=0,y=(int)list[i].length();j<y;j++)
				a[n++]=list[i][j];
		
		/*f[0]=(a[0]=='G')?1:0;
		for(int i=1;i<n;i++)f[i]=(a[i]=='G')?f[i-1]+1:0;*/
		g[n-1]=(a[n-1]=='G')?1:0;
		for(int i=n-2;i>=0;i--)g[i]=(a[i]=='G')?g[i+1]+1:0;

		/*memset(c,0xff,sizeof(c));
		for(int i=0;i<n;i++){
			if(f[i]==m){
				int k=i;
				for(;i<n && (i==k || f[i]!=m);i++)c[i]=k-m+1;
				if(i<n)i--;
			}
		}*/

		memset(d,0xff,sizeof(d));
		for(int i=n-1;i>=0;i--){
			if(g[i]>=m){
				int k=i,e=i+m-1;
				for(;i>=0 && (i==k || g[i]<m);i--)d[i]=e;
				i++;
			}
		}

		for(int i=0;i<=L;i++)h[n][i]=0LL;
		for(int i=n-1;i>=0;i--){
			for(int j=0;j<=L;j++)h[i][j]=h[i+1][j];
			if(d[i]>=0){
				if(i+m-1==d[i]){
					for(int j=m,k=n-i-m+1;j>=0;j--,k+=j?1:0)
						h[i][j]+=(lld)k;
				}else{
					int e=d[i];
					for(int j=g[i],k=e-i-g[i]+(g[i]?1:0);j>=0;j--,k+=j?1:0)
						h[i][j]+=(lld)k;
				}
				continue;
			}
			for(int j=g[i],k=n-i-g[i]+(g[i]?1:0);j>=0;j--,k+=j?1:0)
				h[i][j]+=(lld)k;
		}
		
		for(int s=n-2;s>=0;s--){
			f[s]=(a[s]=='G')?1:0;
			for(int i=s+1;i<n;i++)f[i]=(a[i]=='G')?f[i-1]+1:0;
			for(int i=s+1;i<n;i++)if(f[i-1]>=m)f[i]=m;
			
			for(int i=n-2;i>=s;i--)ans+=h[i+1][m-f[i]];
		}

		for(int i=n-1;i>0;i--){
			if(d[i]<0 || i+m-1==d[i])continue;
			ans+=((lld)(n-d[i]))*((lld)((i+1)*i/2));
		}

		return ans;
	}
};


