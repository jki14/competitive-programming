class CarrotJumping {
public:
	int theJump(int);
};

const int t=1000000007,i=100000;
map<int,int> s;
map<int,int>::iterator sit;

int CarrotJumping::theJump(int x) {
	int min=LONG_MAX,k,a;
	long long k=1,m;
	s.clear();
	for(int i=0;i<=maxi;i++)
	{
		sit=s.find(k);
		if(sit!=s.end())break;
		s[(int)k]=i;
		k*=4;while((k>(long long)t)||(k<0))k%=(long long)t;
	}
	k=1;
	for(int i=0;i<maxi;i++)
	{
		m=k*((long long)x+1);while((m>(long long)t)||(m<0))m%=(long long)t;
		k*=8;while((k>(long long)t)||(k<0))k%=(long long)t;
	}
	return min;
}


