class CarelessSecretary {
public:
	int howMany(int, int);
};

const int maxm=15,maxn=1100;
const long long mod=1000000007;
long long f[maxm][maxn];

long long c(int x,int y)
{
	int i,res=1;
	for(i=0;i<y;i++)
	{
		res*=x--;
		res%=mod;
	}
	while(y)
	{
		res/=y--;
		res%=mod;
	}
	return (long long)res;
}

int CarelessSecretary::howMany(int n, int m) {
	int i,j,k;
	f[0][0]=1;
	for(j=1;j<=n;j++)
	{
		f[0][j]=f[0][j-1]*j;
		f[0][j]%=mod;
	}
	for(i=1;i<=m;i++)
		for(j=1;j<=n;j++)
		{
			f[i][j]=f[0][j];
			for(k=1;k<=i;k++)
			{
				f[i][j]-=f[i-k][j-k]*c(i,k);
				f[i][j]%=mod;
			}
		}
	while(f[m][n]>mod||f[m][n]<0)
	{
		if(f[m][n]>0)
			f[m][n]%=mod;
		else
			f[m][n]+=mod;
	}
	return (int)(f[m][n]%mod);
}


