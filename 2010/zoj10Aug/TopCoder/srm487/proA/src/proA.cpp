class BunnyExamAfter {
public:
	int getMaximum(string, string, string);
};

int BunnyExamAfter::getMaximum(string black, string gray, string white) {
	int res=0;for(int i=0;i<(int)black.length();i++){
		if(black[i]==gray[i])
			res+=(black[i]!=white[i])?1:0;
		else if(black[i]==white[i])
			res+=(black[i]!=gray[i])?1:0;
		else res+=(white[i]==gray[i])?2:1;
	}
	return res;
}


