class AverageAverage {
public:
	double average(vector <int>);
};

double AverageAverage::average(vector <int> a) {
	int n=a.size();
	int i,j,k=1<<n;
	int x,y=0;
	double res=0,ave;
	for(i=1;i<k;i++){
		for(ave=x=0,j=0;j<n;j++)if(((1<<j)&i)>0){
			ave+=(double)a[j];x++;
		}
		res+=ave/x;y++;
	}
	res/=y;
	return res;
}


