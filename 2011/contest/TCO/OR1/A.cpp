class TripleStrings {
public:
	int getMinimumOperations(string, string);
};

int TripleStrings::getMinimumOperations(string init, string goal) {
	int n,k,i,fin;
	n=init.size();
	for(k=0;k<=n;k++){fin=true;
		for(i=k;i<n;i++){
			if(init[i]!=goal[i-k]){
				fin=false;
				break;
			}
		}
		if(fin)break;
	}
	return k*2;
}


